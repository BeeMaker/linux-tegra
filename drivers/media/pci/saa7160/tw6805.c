{
	ULONG frames = 0;

	if( pDevice->FD314ADAD9404496b842EBFC396E875CB & V4L2_STD_525_60 ) {

		frames = 30;
	}

	if( pDevice->FD314ADAD9404496b842EBFC396E875CB & V4L2_STD_625_50 ) {

		frames = 25;
	}
	ULONG R = 0x00000000;

	ULONG counts = 0;

	ULONG i = 0;

	for( i = 0 ; i < frames ; i++ ) {

		counts += fps;

		if( counts >= frames ) {

			counts -= frames;

			R |= (1 << i);
		}
	}
	return R;
}
